#lambda function

add=lambda n1,n2:n1+n2
print(add(10,20))


sub=lambda n1,n2:n1-n2
print(sub(20,10))

max_two=lambda n1,n2:n1 if n1>n2 else n2
print(max_two(20,10))


min_two=lambda n1,n2:n1 if n1<n2 else n2
print(min_two(20,10))


cube=lambda n:n**3
print(cube(3))

multi=lambda n1,n2:n1*n2
print(multi(2,3))








